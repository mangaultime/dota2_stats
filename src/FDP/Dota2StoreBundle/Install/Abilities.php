	public static function createDatabase(Container $container) {
		$dm = $container->get('doctrine_mongodb')->getManager();

		// delete collection ability
		$collection = $dm->getDocumentCollection('\FDP\Dota2StoreBundle\Document\Ability');
		$collection->remove(array());

		// install all albilities
		foreach (Abilities::$listAbilities as $v) {
			$ab = new \FDP\Dota2StoreBundle\Document\Ability();
			$ab->setId($v["id"]);
			$ab->setCodeName($v["codeName"]);
			$ab->setLocalizedName($v["localizedName"]);
			$dm->persist($ab);
			$ab = null;
		}
		$dm->flush();
	}

